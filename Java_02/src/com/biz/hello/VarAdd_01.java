package com.biz.hello;

public class VarAdd_01 {
	public static void main(String[] args) {
		int num1, num2;
		
		num1 = 30;
		num2 = 40;
		
		System.out.println(num1+ " + " +num2 + " = " + (num1+num2));
		System.out.println(num2+ " - " +num1 + " = " + (num2-num1));
		System.out.println(num1+ " * " +num2 + " = " + num1*num2);
		System.out.println(num2+ " / " +num1 + " = " + num2/num1);
		
	}

}
